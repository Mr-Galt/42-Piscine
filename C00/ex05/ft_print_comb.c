#include <unistd.h>

void	ft_putchar(int c)
{
	write(1, &c, 1);
}

void	tripple_putchar(int a, int b, int c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
}

void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = '0';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				tripple_putchar(a, b, c);
				if (a != '7' || b != '8' || c != '9')
				{
					write(1, &",", 1);
					write(1, &" ", 1);
				}
				c++;
			}
			b++;
		}
		a++;
	}
}
